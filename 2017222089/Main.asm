ORG   0000H
LJMP  MAIN   ;跳转至主函数

ORG   0003H
LJMP  INT_X0 ;外部中断0入口

ORG   000BH
LJMP  INT_T0 ;定时器0中断入口

ORG   0013H
LJMP  INT_X1 ;外部中断1入口

ORG   001BH
LJMP  INT_T1 ;定时器1中断入口

ORG   0023H
